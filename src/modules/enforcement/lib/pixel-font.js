const UNKNOWN = [0x1f, 0x11, 0x11, 0x11, 0x11, 0x11, 0x1f];

export const GLYPH_WIDTH = 5;
export const GLYPH_HEIGHT = 7;
/** Advance per character: glyph plus 1px spacing (before scaling). */
export const ADVANCE = GLYPH_WIDTH + 1;

/** @returns {number[]} 7 row bitmasks for a character (uppercased; box when unknown) */
export function glyphFor(char) {
  return G[char.toUpperCase()] ?? UNKNOWN;
}

/** Width in unscaled pixels of a rendered string (no trailing spacing). */
export function textWidth(text, scale = 1) {
  if (text.length === 0) return 0;
  return (text.length * ADVANCE - 1) * scale;
}

/**
 * Iterate the lit pixels of a string. Calls plot(x, y) in unscaled glyph
 * space offset by the character index — the painter applies scaling.
 */
export function eachTextPixel(text, plot) {
  for (let i = 0; i < text.length; i += 1) {
    const rows = glyphFor(text[i]);
    for (let y = 0; y < GLYPH_HEIGHT; y += 1) {
      for (let x = 0; x < GLYPH_WIDTH; x += 1) {
        if (rows[y] & (1 << (GLYPH_WIDTH - 1 - x))) plot(i * ADVANCE + x, y);
      }
    }
  }
}
